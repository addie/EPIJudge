def findCount(arr, num):
    def find(arr, num, find_first):
        s, e = 0, len(arr) - 1
        res = None
        while s <= e:
            m = (s + e) // 2
            if num < arr[m]:
                e = m - 1
            elif num > arr[m]:
                s = m + 1
            else:
                res = m
                if find_first:
                    e = m - 1
                else:
                    s = m + 1

        return res

    s = find(arr, num, True)
    e = find(arr, num, False)
    return e - s + 1 if s is not None else 0

